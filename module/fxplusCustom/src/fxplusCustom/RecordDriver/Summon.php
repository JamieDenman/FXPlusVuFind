namespace fxplusCustom\RecordDriver;

/**
 * Model for Summon records.
 *
 * @category VuFind
 * @package  RecordDrivers
 * @author   Demian Katz <demian.katz@villanova.edu>
 * @license  http://opensource.org/licenses/gpl-2.0.php GNU General Public License
 * @link     https://vufind.org/wiki/development:plugins:record_drivers Wiki
 */
class Summon extends \VuFind\RecordDriver\Summon
{
    /**
     * Return an array of associative URL arrays with one or more of the following
     * keys:
     *
     * <li>
     *   <ul>desc: URL description text to display (optional)</ul>
     *   <ul>url: fully-formed URL (required if 'route' is absent)</ul>
     *   <ul>route: VuFind route to build URL with (required if 'url' is absent)</ul>
     *   <ul>routeParams: Parameters for route (optional)</ul>
     *   <ul>queryString: Query params to append after building route (optional)</ul>
     * </li>
     *
     * @return array
     */
    public function getURLs()
    {
        if (isset($this->fields['link'])) {
            return [
                [
                    'url' => $this->fields['link'],
                    'desc' => $this->translate('Get Summon full text')
                ]
            ];
        }
        $retVal = [];
        if (isset($this->fields['url']) && is_array($this->fields['url'])) {
            foreach ($this->fields['url'] as $desc => $url) {
                $retVal[] = ['url' => $url, 'desc' => $desc];
            }
        }
        return $retVal;
    }


}